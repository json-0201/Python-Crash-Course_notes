# 4-1. Pizzas
pizzas = ['four cheese', 'carbonara', 'bbq']

num = 1
for pizza in pizzas:
    print(f'pizza #{num}: {pizza.title()}')
    num += 1

print('\nI can eat pizzas everyday!\n')

# 4-2. Animals
animals = ['tiger', 'lion', 'jaguar']

num = 1
for animal in animals:
    print(f'animal #{num}: {animal.title()}')
    num += 1

print('\nI would love to have any of these animals as my pet!\n')
