# Integers
# +, -, *, /
# ** - exponent
# Python supports order of operation

print((2+3)**4)

# Floats - for number with a decimal
# division, even between integers - result in float
# mixing integer and float - result in float

print(4/2)
print(2*2.3)

# underscores in numbers - for better readability

num = 23_000_132_425_142 # num = 23000132425142
print(num)

# multiple assignment - reduce number of lines in a code

x, y, z = 4, 8, 6
print(x, y, z)

# constants - use all capital letters, value is never changed
MAX_SPEED = 1000
print(MAX_SPEED)